import mongoose from 'mongoose';

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true, // Important to avoid duplicate accounts
    lowercase: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    default: 'Free',
  },
}, {
  timestamps: true, // Adds createdAt and updatedAt fields
});

export default mongoose.model('User', userSchema);

